public class VarDemo {
  public static void main(String[] args) {
    var vag = 10.0;
    System.out.println("Value of vag: " + vag);
    int var = 1;
    System.out.println("Value of var: " + var);
    // In this case var is both types of declartions and initilizer
    var k = - var;
    System.out.println("Value of k: " + k);
  }
}
